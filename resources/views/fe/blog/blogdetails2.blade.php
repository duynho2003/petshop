                                <h1>WHY DO YOU TRAIN YOUR DOG?</h1>
                                <p>You and I probably have a lot of overlapping reasons, and I suspect you have some unique to you. Maybe you train your dog for basic safety (yours, his/hers, other people and pets, etc.) or for the fun of doing tricks or doggy dancing together. Maybe you train your dog to perform tasks for you. Maybe you train your dog as a hobby to keep yourself busy or to strengthen your bond. Maybe you compete or show or hunt.</p>

                                <p>When Cooper was a puppy, my “why” for training with him was to help him learn to manage his fears and to stimulate his incredibly brilliant imagination… or else he found ways to keep himself entertained, ways that often resulted in property damage. When he was an adolescent, I trained Cooper to run safely by my side and to return when called. Now that he’s almost 13, we train for fun and to keep his brain and body active.</p>

                                <p>With Emmett, we trained hard so he could be a working dog in animal-assisted therapy settings, and with Lucas, we trained so he wouldn’t eat anyone. For both those big boys, and for Cooper, training also helped them navigate the human world safely and to build our bond.</p>

                                <p>My “why” for dog training can be distilled down to those three basic things: safety, security, and comfort (theirs and mine on all three counts).</p>

                                <p>What are your “whys” for training with your dog?</p>

