                                <h4 class="title">WHAT IS YOUR “WHY”? WHAT IS YOUR DOG’S?</h4>
                                <p>This post meanders, y’all. Feel free to come back another day if you don’t want a string of thoughts about Google, AI, dog training, and blogging.</p>
                                <p>Unless you’re someone who works online, you might have missed this big piece of news from Google: Google is going to begin using AI to change how it presents search engine results. You can
                                    <a href="https://www.forbes.com/sites/mattnovak/2023/05/11/google-is-about-to-drop-a-nuclear-bomb-on-the-online-publishing-industry/?sh=5db6e0783faa" target="_blank" rel="noreferrer noopener">read the whole story here</a> It’s interesting, to say the least.
                                </p>

                                <p>If you’re someone who uses the internet for research, shopping, connection, and so on, these changes will make all that easier for you. Instead of having to click into websites to read the full details on your search, Google’s AI will aggregate the results into a couple short paragraphs that answer your question pulling from several sources. Easy!</p>

                                <p>As for content creators, this introduces a whole new set of challenges. If Google’s AI aggregates the best info to answer your question and presents it in a seamless way, well, people won’t be clicking on your site to read the post anymore. Clicks create views. Views create ad or affiliate revenue. No clicks, no money.</p>

                                <p>There are questions as to whether this generative AI can be considered plagiarism, among other concerns, but the collective head-exploding sound you heard was from content creators who predict massive drops in pageviews.</p>

                                <p>And that stinks. I believe anyone who creates valuable, useful, entertaining content deserves to earn a living from that hard, painstaking, time-consuming work. (On the flip side, it WILL penalize all those junky affiliate sites that are nothing but keyword stuffing, so that’s good.)</p>

                                <p>I don’t believe this is the death toll for blogs, though many seem to think so.</p>

                                <p>To me, it circles back to one question: Why are you blogging in the first place?</p>

                                <p>But doesn’t that question underpin just about everything we do? Why are you blogging / writing / dog training / scrolling social media / working extra shifts / etc.?</p>

                                <p>The reasons <b>why</b> we do things matters more than the individual tasks, no?</p>

                                <p><b>This has a lot of overlap with those of us who are devout dog (and cat) lovers.</b></p>

                                <p>Stick with me here.</p>

                                <p>Let’s pivot to dogs for a sec.</p>

